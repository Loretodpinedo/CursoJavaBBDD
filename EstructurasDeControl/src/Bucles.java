
public class Bucles {

	public static void main(String[] args) {

		// While
		int count = 1;
		while (count < 11) {
			System.out.println("El numero calculado con while es: " + count);
			count++;
		}

		// Do while

		do {
			System.out.println("El numero calculado con do while es  " + count);
			count++;
		} while (count <= 11);

		// For
		// inicializacion;terminacion;incremento o decremento
		for (int i = 1; i < 11; i++) {
			System.out.println("El numero calculado con for es " + i);

		}
		
		//For each
		//TipoARecorrer nombreVariableTemporal:nombreColeccion

	}

}
