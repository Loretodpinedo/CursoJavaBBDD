package ActividadPractica1;

public class OperacionesMatematicas {

	public static void main(String[] args) {

		int operando1 = 102;
		int operando2 = 32;

		System.out.println("El resutado de " + operando1 + " + " + operando2 + " es: " + (operando1 + operando2));
		System.out.println("El resutado de " + operando1 + " - " + operando2 + " es: " + (operando1 - operando2));
		System.out.println("El resutado de " + operando1 + " / " + operando2 + " es: " + (operando1 / operando2));
		System.out.println("El resutado de " + operando1 + " * " + operando2 + " es: " + (operando1 * operando2));

	}

}
